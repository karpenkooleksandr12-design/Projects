
a = float(input("Введіть половину сторони прямокутника по осі X: "))
b = float(input("Введіть половину сторони прямокутника по осі Y: "))

x = float(input("Введіть координату x точки T: "))
y = float(input("Введіть координату y точки T: "))

if abs(x) <= a and abs(y) <= b:
    print("Точка належить прямокутнику.")
else:
    print("Точка не належить прямокутнику.")
